package cn.wanda.entity;

public class Reader {
	
	private Integer id;
	private User jikeUser;
	private Integer money;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public User getJikeUser() {
		return jikeUser;
	}

	public void setJikeUser(User jikeUser) {
		this.jikeUser = jikeUser;
	}

	public Integer getMoney() {
		return money;
	}

	public void setMoney(Integer money) {
		this.money = money;
	}

}
